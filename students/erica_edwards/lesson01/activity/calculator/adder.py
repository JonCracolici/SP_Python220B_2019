"""
This module provides an addition operator
"""


class Adder():
    """
    Addition methods
    """

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Perform addition calculation

        Args:
            operand_1 (int): 1st operand
            operand_2 (int): 2nd operand

        Returns:
            int: result
        """
        return operand_1 + operand_2
